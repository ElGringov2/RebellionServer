function ReadDocAttribute($property, $AttributeName)
{
    $name = "";
    if (preg_match('/@' . $AttributeName . '\s+([^\s]+)/', $property->getDocComment(), $matches)) {
        list(, $name) = $matches;
    }
    return $name;
}



function ToXML($object)
{
    $class = get_class($object);
    $reflector = new ReflectionClass($class);
    $properties = $reflector->getProperties();
    $methods = $reflector->getMethods();
    $str = "<" . strtolower($class) . " ";
    foreach ($properties as $property) {
        $name = ReadDocAttribute($property, "XmlAttribute");
        if ($name == "")
            $name = $property->getName();

        $str .= "$name=\"{$property->getValue($object)}\" ";


    }
    foreach ($methods as $method) {
        $name = ReadDocAttribute($method, "XmlAttribute");
        if ($name != "") {
            $str .= "$name=\"{$method->Invoke($object)}\" ";

        }

    }
    $str .= "/>";


    return $str;
}
























